#define int long long
const int oo = 1e18, K = 30;

void solve(){
	int n; cin >> n;
	vector<int>a(n+1);
	for (int i = 1; i<=n; i++) cin >> a[i];
	vector<vector<int>>g(n+1);
	for (int i = 1; i<n; i++){
		int a, b; cin >> a >> b;
		g[a].emplace_back(b);
		g[b].emplace_back(a);
	}
	int ans = 1;
	vector<int>dp(n+1);

	function<void(int, int)>dfs = [&](int v, int pa){
		int i = lower_bound(dp.begin(), dp.end(), a[v])-dp.begin();
		int curr = dp[i];
		dp[i] = a[v];
		ans = max(ans, i);
		for (auto x: g[v]){
			if (x == pa) continue;
			dfs(x, v);
		}
		dp[i] = curr;	
	};

	for (int i = 1; i<=n; i++) {
		dp.assign(n+1, oo);
		dfs(i, i);
	}
	cout << ans+1 << "\n";
}

int32_t main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	solve();
	
	return 0;
}