#define int long long

void solve(){
	int n, k; cin >> n >> k;
	vector<int>a(n), open(n);
	for (int i = 0; i<n; i++) cin >> a[i];
	int curr_open = 0, sum = 0;
	int ans = 0;
	for (int i = n-1; i>=0; i--){
		sum -= curr_open;
		curr_open -= open[i];
		a[i] -= sum;
		a[i] = max(a[i], 0LL);
		if (!a[i]) continue;
		int el = min(i+1, k);
		int tmp = (a[i]+el-1)/el;
		sum += tmp*el;
		curr_open += tmp;
		ans += tmp;
		if (i>=el) open[i-el]+=tmp;
	}
	cout << ans << "\n";
}

int32_t main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	int t = 1; //cin >> t;
	while (t--) solve();
	
	return 0;
}
