const int oo = 1e18, oo2 = 1e9+7, K = 30;
const int mod = 998244353;

void solve(){
    int n; cin >> n;
    if (n == 2){
        cout << "1 3\n";
        return;
    }
    int x = ((n-1)*n)/2 - 1;
    int L = 1, R = oo2;
    int ans = oo2;
    while (R >= L){
        int mid = (L+R)/2;
        if (mid*mid - 3 * mid - 1 >= x){
            ans = mid;
            R = mid-1;
        } else {
            L = mid+1;
        }
    }
    cout << "1 ";
    for (int i = 0; i < n-3; i++){
        cout << i+2 << " ";
        x -= (i+2);
    }
    cout << x << " " << ans << "\n";
}

int32_t main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    int t = 1;
    cin >> t;
    while (t--) solve();

    return 0;
}