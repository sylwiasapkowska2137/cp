void solve(){
	int n; cin >> n;
	vector<int>p(n+1);
	for (int i = 1; i<=n;i++) cin >> p[i];
	vector<vector<int32_t>>dpL(n+2, vector<int32_t>(n+2, 0));
	vector<vector<int32_t>>dpR(n+2, vector<int32_t>(n+2, 0));
	for (int i = 1; i<=n; i++){
		for (int val = 1; val<=n; val++){
			dpL[i][val] = dpL[i-1][val] + (p[i] < val);
		}
	}
	for (int i = n; i>=1; i--){
		for (int val = 1; val<=n; val++){
			dpR[i][val] = dpR[i+1][val] + (p[i] < val);
		}
	}
	int ans = 0;
	for (int b = 2; b<=n-2; b++){
		for (int c = b+1; c<=n-1; c++){
			ans += dpL[b-1][p[c]]*dpR[c+1][p[b]];
		}
	}
	cout << ans << "\n";
}

int32_t main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	int t = 1; cin >> t;
	while (t--) solve();
	
	return 0;
}