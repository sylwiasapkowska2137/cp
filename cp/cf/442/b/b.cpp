#define int long long
const int oo = 1e18, K = 30;
typedef long double ld;

void solve(){
	int n; cin >> n;
	vector<ld>p(n);
	for (int i = 0; i<n; i++) cin >> p[i];
	sort(p.rbegin(), p.rend());
	if (p[0] == 1){
		cout << 1.0 << "\n";
		return;
	}
	ld ans = 0, prod = 1, sum = 0;
	for (int i = 0; i<n; i++){	
		prod *= (1.0-p[i]);
		sum += (p[i]/(1.0-p[i]));
		ans = max(ans, prod*sum);
	}
	cout << fixed << setprecision(20) << ans << "\n";
}

