bool check(int mid){
	cout << "? 1 " << mid << "\n";
	cout.flush();
	vector<int>a(mid);
	for (auto &x: a) cin >> x;
	int cnt = 0;
	for (auto x: a){
		if (x <= mid) cnt++;
	}
	if (cnt&1) return 1;
	return 0;
}

void solve(){
	int n; cin >> n;
	int L = 1, R = n, ans = n;
	while (R>=L){
		int m = (L+R)/2;
		if (check(m)){
			ans = m;
			R = m-1;
		} else L = m+1;
	}
	cout << "! " << ans << "\n";
	cout.flush();
}

