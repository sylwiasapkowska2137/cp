const int oo = 1e18, K = 30;

void solve(){
	int n, k; cin >> n >> k;
	string s; cin >> s;
	int ile = 1, x = 0;
	for (int i = 1; i<n; i++){
		if (s[i] != s[i-1]){
			if (s[i-1] == 'B') x = max(x, ile);
			ile = 0;
		} 
		ile++;
		
	}
	if (s.back() == 'B') x = max(x, ile);
	if (x >= k) cout << "0\n";
	else {
		map<char, int>cnt;
		for (int i = 0; i<k-1; i++){
			cnt[s[i]]++; 
		}
		int ans = oo;
		for (int i = k-1; i<n; i++){
			cnt[s[i]]++;
			ans = min(ans, cnt['W']);
			cnt[s[i-(k-1)]]--;
		}
		cout << ans << "\n";
	}
}

int32_t main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	int t = 1; cin >> t;
	while (t--) solve();
	
	return 0;
}