void solve(){
	int n, q; cin >> n >> q;
	vector<int>a(n);
	for (int i = 0; i<n; i++) cin >> a[i];
	sort(a.begin(), a.end());
	vector<int>pref(n+1);
	for (int i = 0; i<n; i++){
		pref[i+1] = pref[i]+a[i];
	}

	while (q--){
		int x; cin >> x;
		int d = (int)(lower_bound(a.begin(), a.end(), x)-a.begin());
		debug(d);
		cout << d*x-pref[d]+(pref[n]-pref[d])-(n-d)*x << "\n";
	}
}

