void solve(){
	int n, m; cin >> n >> m;
	vector<int>s(n);
	for (int i = 1; i<=n-1; i++) cin >> s[i];
	vector<int>x(m);
	for (int i = 0; i<m; i++) cin >> x[i];
	vector<int>b(n+1);
	for (int i = 2; i<=n; i++) b[i] = s[i-1]-b[i-1];
	map<int, int>cnt;
	for (int i = 1; i<=n; i++){
		for (int j = 0; j<m; j++){
			int c = x[j]-b[i];
			if (!(i&1)) c = -c;
			cnt[c]++;
		}
	}
	int ans = 0;
	for (auto x: cnt) ans = max(ans, x.second);
	cout << ans << "\n";
}

int32_t main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	int t = 1; 
	while (t--) solve();
	
	return 0;
}
