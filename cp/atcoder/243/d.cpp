void solve(){
	int n; LL x; cin >> n >>  x;
	string s; cin >> s;
	vector<int>v;
	for (auto c: s){
		if (c == 'L') v.push_back(0);
		if (c == 'R') v.push_back(1);
		if (c == 'U'){
			if(!v.empty() && v.back() >= 0) v.pop_back();
			else v.push_back(-1);
		}
	}
	for (auto c: v){
		if (c >= 0) x = 2*x+c;
		else x/=2;
	}
	cout << x << "\n";
}

