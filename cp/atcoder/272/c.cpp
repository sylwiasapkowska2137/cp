void solve(){
	int n; cin >> n;
	vector<int>p, np;
	for (int i = 1; i<=n; i++){
		int x; cin >> x;
		if (x&1) np.emplace_back(x);
		else p.emplace_back(x);
	}
	int ans = -1;
	for (int rep = 0; rep < 2; rep++){
		sort(p.rbegin(), p.rend());
		while (p.size() > 4) p.pop_back();
		for (int i = 0; i<(int)p.size(); i++){
			for (int j = i+1; j<(int)p.size(); j++){
				if ((p[i]+p[j])%2 == 0) {
					ans = max(ans, p[i]+p[j]);
				}
			}
		}
		swap(p, np);
	}
	cout << ans << "\n";
}

