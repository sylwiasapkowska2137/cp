struct Edge{
    int from, to, capacity, cost;
};

vector<vector<int>> adj, cost, capacity;
const int INF = 1e16;

void shortest_paths(int n, int v0, vector<int>& d, vector<int>& p) {
    d.assign(n, INF);
    d[v0] = 0;
    vector<bool> inq(n, false);
    queue<int> q;
    q.push(v0);
    p.assign(n, -1);

    while (!q.empty()) {
        int u = q.front();
        q.pop();
        inq[u] = false;
        for (int v : adj[u]) {
            if (capacity[u][v] > 0 && d[v] > d[u] + cost[u][v]) {
                d[v] = d[u] + cost[u][v];
                p[v] = u;
                if (!inq[v]) {
                    inq[v] = true;
                    q.push(v);
                }
            }
        }
    }
}

vector<int> min_cost_flow(int N, vector<Edge> edges, int K, int s, int t) {
    adj.assign(N, vector<int>());
    cost.assign(N, vector<int>(N, 0));
    capacity.assign(N, vector<int>(N, 0));
    for (Edge e : edges) {
        adj[e.from].push_back(e.to);
        adj[e.to].push_back(e.from);
        cost[e.from][e.to] = e.cost;
        cost[e.to][e.from] = -e.cost;
        capacity[e.from][e.to] = e.capacity;
    }

    int flow = 0;
    int cost = 0;
    vector<int> d, p, ans;
    while (flow < K) {
        shortest_paths(N, s, d, p);
        if (d[t] == INF)
            break;

        // find max flow on that path
        int f = K - flow;
        int cur = t;
        while (cur != s) {
            f = min(f, capacity[p[cur]][cur]);
            cur = p[cur];
        }

        // apply flow
        flow += f;
        cost += f * d[t];
        cur = t;
        while (cur != s) {
            capacity[p[cur]][cur] -= f;
            capacity[cur][p[cur]] += f;
            cur = p[cur];
        }
		ans.emplace_back(cost);
	}
	return ans;
}

void solve(){
	int n; cin >> n;
	vector<vector<int>>w(M, vector<int>(M, 0));
	for (int i = 0; i<n; i++){
		int a, b, c; cin >> a >> b >> c;
		b += N;
		w[a][b] = max(w[a][b], c);
	}
	//0 - sink
	//1-150 - A
	//151-300 - B
	//301 - target
	vector<Edge>graph;
	for (int i = 1; i<=150; i++){
		for (int j = 151; j<=300; j++){
			if (w[i][j]){
				graph.push_back({i, j, 1, -w[i][j]});
			}
		}
	}
	int t = 301;
	for (int i = 1; i<=150; i++){
		graph.push_back({0, i, 1, 0});
		graph.push_back({i+150, t, 1, 0});
	}
	vector<int>ans = min_cost_flow(M, graph, 160, 0, t);
	cout << (int)ans.size() << "\n";
	for (auto x: ans) cout << -x << "\n";
}

