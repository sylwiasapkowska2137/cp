#define int long long
const int oo = 1e18, K = 30;
vector<vector<int>>ans;
int n, m;

void gen(vector<int>curr){
	if (curr.size() == n){
		ans.emplace_back(curr);
		return;
	}
	for (int i = 1; i<=m; i++){
		if (curr.empty() || curr.back() < i){
			curr.emplace_back(i);
			gen(curr);
			curr.pop_back();
		}
	}
}

void solve(){
	cin >> n >> m;
	gen({});
	sort(ans.begin(), ans.end());
	for (auto x: ans){
		for (auto y: x){
			cout << y << " ";
		}
		cout << "\n";
	}
}

