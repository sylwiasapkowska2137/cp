void solve(){
	int n; cin >> n;
	vector<int>a(n);
	for (int i = 0; i<n; i++) cin >> a[i];
	LL s = accumulate(a.begin(), a.end(), 0LL);
	sort(a.begin(), a.end());
	vector<int>b;
	int L = 0, R = n-1;
	while (1){
		b.push_back(a[L]);
		L++;
		if (L > R) break;
		b.push_back(a[R]);
		R--;
		if (L > R) break;
	}
	//debug(b);
	for (int i = 1; i<n; i++){
		s += max(0, b[i]-b[i-1]);
	}
	cout << s << "\n";
}

int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	solve();
	
	return 0;
}
