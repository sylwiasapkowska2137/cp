typedef long double LD;

struct elem{
	LD a, b; int i;
	elem (LD _a = 0, LD _b = 0, int _i = 0){
		a = _a, b = _b, i = _i;
	}
};

void solve(){
	int n; cin >> n;
	vector<elem>tab;
	for (int i = 0; i<n; i++){
		LD a, b; cin >> a >> b;
		tab.emplace_back(a, b, i+1);
	}
	sort(tab.begin(), tab.end(), [](auto a, auto b){
		return a.a*b.b > a.b*b.a;
	});
	for (auto [a, b, i]:tab){
		cout << i << '\n';
	}
}

int main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	solve();
	
	return 0;
}
