void solve(){
	int m, n; cin >> m >> n;
	vector<pair<int, int>>a(n);
	for (int i = 0; i<n; i++) cin >> a[i].first >> a[i].second;
	stable_sort(a.rbegin(), a.rend());
	set<int>s;
	int ans = 0;
	for (int i = 1; i<=m; i++) s.insert(i);
	for (auto [val, h]: a){
		if (s.empty()) break;
		if (*s.rbegin() < h) continue;
		auto it = s.lower_bound(h);
		ans += val;
		s.erase(it);
	}
	cout << ans << "\n";
}

int32_t main(){
	ios_base::sync_with_stdio(0);
	cin.tie(0);
	cout.tie(0);

	solve();
	
	return 0;
}
