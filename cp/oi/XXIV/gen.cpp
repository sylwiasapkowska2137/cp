mt19937 rng(chrono::steady_clock().now().time_since_epoch().count());
int p(int a, int b){
	return a+rng()%(b-a+1);
}

void solve(){
	int n = p(1, 10);
	cout << n << "\n";
	for (int i = 2; i<=n; i++){
		cout << i << " " << p(1, i-1) << "\n";
	}
	int q = p(1, 5);
	cout << q << "\n";
	vector<int>del(n+1);
	while (q--){
		int v = p(1, n);
		if (del[v]) cout << -v << "\n";
		else cout << v << "\n";
		del[v] = (del[v]^1);
	}
}

